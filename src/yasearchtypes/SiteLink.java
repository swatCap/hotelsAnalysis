package yasearchtypes;

/**
 *
 * @author margarita
 */
public class SiteLink {
    private String name;
    private String url;

    public SiteLink(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public String getUrl() {
        return url;
    }
    
    
}
